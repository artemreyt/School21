#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	while (*s1 != '\0' && (t_uchar)(*s1) == (t_uchar)(*s2))
	{
		++s1;
		++s2;
	}
	return ((int)((t_uchar)(*s1) - (t_uchar)(*s2)));
}
