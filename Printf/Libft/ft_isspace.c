#include "libft.h"

int			ft_isspace(int c)
{
	t_uchar	sp;

	sp = (t_uchar)c;
	return (sp == ' ' || sp == '\n' || sp == '\t' ||
			sp == '\r' || sp == '\v' || sp == '\f');
}
