#include <fcntl.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*(str++));
}

int		main(int argc, char **argv)
{
	int		fd;
	int		indicator;
	char	buffer[1024];

	if (argc == 1)
		ft_putstr("File name missing.\n");
	else if (argc > 2)
		ft_putstr("Too many arguments.\n");
	else
	{
		fd = open(argv[1], O_RDONLY);
		while ((indicator = read(fd, buffer, 1023)) > 0)
		{
			buffer[indicator] = '\0';
			ft_putstr(buffer);
		}
		close(fd);
	}
	return (0);
}
